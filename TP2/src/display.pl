print_header :- write('---------------------\n').

print_line(_, 0) :- write('|\n').
print_line([H|T], N) :- 
    N > 0,
    write('|'),
    write(H),
    N1 is N-1,
    print_line(T, N1).

print_matrix(_, 0).
print_matrix([H|T], N) :-
    N > 0,
    print_line(H, 10),
    N1 is N-1,
    print_matrix(T, N1).

display_game(Board) :-
    begin_state(Board), /* to remove */
    clear,
    print_header,
    print_matrix(Board, 10),
    print_header,
    write('end').